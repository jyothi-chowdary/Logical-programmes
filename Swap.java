import java.util.Scanner;
public class Swap{
public static void main(String[]args){
		int a=20;
		int b=10;
		System.out.println("before swap");
		System.out.println("a:"+a);
		System.out.println("b:"+b);
		a=a+b;
		b=a-b;
		a=a-b;
		System.out.println("after swap");
		System.out.println("a:"+a);
		System.out.println("b:"+b);
}
}


		

